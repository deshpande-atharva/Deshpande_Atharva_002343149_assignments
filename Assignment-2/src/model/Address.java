public class Address {
    private String streetAddress;
    private Integer unitName; //for clearing the value after person is added. If we use int it gives error
    private String  city;
    private String state;
    private Integer zipCode;//int datatype
    private String phoneNumber;//doble dataype

    public String getStreetAddress() {
        return streetAddress;
    }

    public void setStreetAddress(String streetAddress) {
        this.streetAddress = streetAddress;
    }

    public Integer getUnitName() {
        return unitName;
    }

    public void setUnitName(Integer unitName) {
        this.unitName = unitName;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getState() {
        return state;
    }

    public void setState(String state) {
        this.state = state;
    }

    public Integer getZipCode() {
        return zipCode;
    }

    public void setZipCode(Integer zipCode) {
        this.zipCode = zipCode;
    }

    public String getPhoneNumber() {
        return phoneNumber;
    }

    public void setPhoneNumber(String phoneNumber) {
        this.phoneNumber = phoneNumber;
    }
    
    
}
